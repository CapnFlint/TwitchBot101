'''
Variables and Operators
'''

# Data Types
foo = 1             # number
foo = 1.1           # float
foo = "a string"    # string
foo = print         # function reference
foo = MyClass()     # Class
foo = [1,"two"]     # List
foo = (1, 2, 3)     # Tuple (Immutable)
foo = {             # Dictionary
    "a":"foo",
    "b":42
}
foo = None          # Null/None type

# Operators
foo = 1     # assignment
1 + 1       # arithmetic
"a" + "b"   # concatenation
foo == bar  # comparison
foo += 1    # arithetic + assignment

# Accessors/references

foo         # variable reference
foo[0]      # List reference
foo['bar']  # Dictionary reference
MyClass.foo # Class variable reference
